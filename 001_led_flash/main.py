# ------------------------------------------------------
# Import modules
# ------------------------------------------------------
import RPi.GPIO as GPIO
import time
# ======================================================

# ------------------------------------------------------
# Pin definition
# ------------------------------------------------------
pin_no = 17
# ======================================================

# ------------------------------------------------------
# Main Process Function
# ------------------------------------------------------
def main():
  # Setup
  GPIO.setmode(GPIO.BCM)
  GPIO.setup(pin_no, GPIO.OUT)

  try:  
    # Loop
    while True:
        GPIO.output(pin_no, GPIO.HIGH)
        time.sleep(1)
        GPIO.output(pin_no, GPIO.LOW)
        time.sleep(1)
  except KeyboardInterrupt:
    pass

  GPIO.cleanup()
# ======================================================

# ------------------------------------------------------
# Program Start
# ------------------------------------------------------
if __name__ == '__main__':
    main()